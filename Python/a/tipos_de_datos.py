# Variables y tipos de datos
nombre = "Alumno"  # String (cadena de texto)
edad = 30  # Integer (entero)
altura = 1.75  # Float (decimal)
es_estudiante = True  # Boolean (booleano)

# Listas
frutas = ["manzana", "banana", "naranja"]

# Agregar un elemento a la lista
frutas.append("pera")
# Eliminar un elemento de la lista
frutas.remove("banana")

# Diccionarios
persona = {
    "nombre": "Alumno",
    "edad": 30,
    "altura": 1.75
}

# Agregar un nuevo par clave-valor al diccionario
persona["es_estudiante"] = True

# Operaciones Aritméticas
a = 10
b = 3

# Suma
suma = a + b  # 13

# Resta
resta = a - b  # 7

# Multiplicación
multiplicacion = a * b  # 30

# División
division = a / b  # 3.333...

# División entera (floor division)
division_entera = a // b  # 3

# Resto (módulo)
resto = a % b  # 1

# Potenciación
potencia = a ** b  # 1000 (10^3)

# Operaciones de Comparación
# Igualdad
igual = (a == b)  # False

# Desigualdad
desigual = (a != b)  # True

# Mayor que
mayor_que = (a > b)  # True

# Menor que
menor_que = (a < b)  # False

# Mayor o igual que
mayor_o_igual = (a >= b)  # True

# Menor o igual que
menor_o_igual = (a <= b)  # False